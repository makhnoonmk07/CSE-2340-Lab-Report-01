
package java_operators.c251227;


public class Java_OperatorsC251227 {

    
     public static void main(String[] args) {
        int x = 10;
        int y = 3;
       //arithmetic Operators
     System.out.println(x + y); 
     System.out.println(x - y); 
     System.out.println(x * y); 
     System.out.println(x / y); 
     System.out.println(x % y); 

    int z = 5;
    ++z;
    System.out.println(z); 
    --z;
    System.out.println(z); 

   //Assignment Operator
     x +=5;
     System.out.println(x);


    //Comparison Operator
     System.out.println(x>y);
     System.out.println(x<y);
  
     
     //Logical Operator
      System.out.println(x > 3 && y < 10); 
    }
    
}
