
package java_break_continue.c251227;

public class Java_Break_ContinueC251227 {

    public static void main(String[] args) {
         for (int i = 0; i < 5; i++) {
      if (i == 2) {
        continue;
      }
      if (i == 4) {
        break;
      }
      System.out.println(i);
    }
    }
    
}
